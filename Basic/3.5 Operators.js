// **  Exponentiation
let y = 3;
let x = y ** 2; // means y = 3, so 3 * 3 = 9
//console.log(x); //9

//++ Pre increment

let a = 6;
let b = ++a // 1 + a = 7
console.log(b); // b = a, so b = 7
console.log(a); // 7

//++ Post increment

let d = 6;
let e = d++ // d + 1 = 7
console.log(e); // 6
console.log(d); // 7