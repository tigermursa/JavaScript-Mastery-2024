/*
?Ternary operator
The conditional operator, also known as the ternary operator, is a concise way to write conditional statements in JavaScript. 
It provides a shorthand syntax for expressing simple if-else statements.

condition ? expression_if_true : expression_if_false;
*/

const age = 10;
const message = age >= 18 ? "You are an adult!" : "You are a minor!";
console.log(message); // Output: 'You are an minor!'
